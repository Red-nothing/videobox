/**
 * Class ModuleVideoBoxReader
 *
 * @copyright  certo web & design GmbH 2010 - 2011 
 * @author     Yanick Witschi <yanick.witschi@certo-net.ch> 
 * @package    Controller
 */
class ModuleVideoBoxReader extends Module
{

	/**
	 * Template
	 * @var string
	 */
	protected $strTemplate = 'mod_videobox_reader';


	/**
	 * Display a wildcard in the back end
	 * @return string
	 */
	public function generate()
	{
		if (TL_MODE == 'BE')
		{
			$objTemplate = new BackendTemplate('be_wildcard');
			$objTemplate->wildcard = '### VIDEOBOX READER ###';

			$objTemplate->title = $this->headline;
			$objTemplate->id = $this->id;
			$objTemplate->link = $this->name;
			$objTemplate->href = 'typolight/main.php?do=modules&amp;act=edit&amp;id=' . $this->id;

			return $objTemplate->parse();
		}
        
        if (!$this->Input->get('video'))
        {
            return '';
        }

		return parent::generate();
	}


	/**
	 * Generate the module
	 */
	protected function compile()
	{
	    $this->Template->video = new VideoBoxElement($this->Input->get('video'));
    }
}
	